	AIDS ( Acquired Immune Deficiency Syndrome ) adalah kumpulan gejala yang diakibatkan berkurangnya kekebalan tubuh,
	penyebabnya adalah virus yang mudah menular dan merusak sel darah putih sebagai pembawa kekebalan tubuh. 
	Virus penyebab AIDS disebut HIV (Human Immunodeficiency Virus).
	<br>
	<br>
	Virus HIV ini terdapat pada darah, cairan mani, cairan liang sanggama dan cairan tubuh yang lain yang berasal
	dari penderita AIDS. Namun yang terbukti dapat menularkan adalah darah, cairan mani dan cairan lain sanggama.
	<br>
	<br>

	Penularan AIDS menular melalui :
	<br>
	1. Hubungan Seksual, baik antar sesama jenis (homoseksual) maupun berlainan jenis (heteroseksual).
	<br>
	2. Transfusi dengan darah dan produk- produk darah yang berasal dari penderita AIDS.
	<br>
	3. Pemakaian jarum suntik yang tercemar HIV (tindik, tusuk jarum, tattoo)
	<br>
	4. Dari ibu ke bayinya waktu persalinan atau lewat ASI.
	<br>

	
	<br>
	<br>
	Jadi AIDS tidak menular melalui gigitan nyamuk/serangga, berjabatan tangan, berangkulan, bersin/batuk,
	ciuman persahabatan, duduk makan bersama, penggunaan WC yang sama,kolam renang.
	<br>
	<b>Bila seseorang yang sehat tertular HIV :</b>
	<br>
	-Selama 3 – 6 bulan dalam darahnya umumnnya belum ditemukan HIV ( test darah negative )
	<br>
	-Setelah 3 – 6 bulan, test darahnya akan menunjukkan HIV +
	<br>
	-5 – 10 tahun kemudian, bila tidak diobati akan timbul gejala – gejala AIDS meliputi letih, lesu, berta badan menurun drastis, 
	demam/panas lebih dari 1 bulan, diare lebih dari 1 bulan, sesak napas, pembesaran kelenjar getah bening, sariawan yang lama, penyakit kulit (Sarcoma Kaposi), infeksi- infeksi lainnya.
	<br>
	<br>
	AIDS adalah penyakit menular, bukan penyakit turunan atau kutukan Tuhan. Sampai sekarang belum ditemukan obat yang mematikan virus AIDS,
	namun sudah ada obat ARV yang dapat memperlambat perkembangan virus dalam tubuh. Bila termasuk kelompok beresiko tinggi, 
	periksakan diri anda secara teratur sehingga bisa menolong diri sendiri dan lingkungan
