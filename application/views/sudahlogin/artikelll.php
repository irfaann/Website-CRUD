<b>          
KOMINFO
</b>
<br>
Kementerian Komunikasi dan Informatika (Kemenkominfo) kembali mengingatkan dan mengajak masyarakat di
 Indonesia untuk berperan aktif dan mendukung pemerintah dalam upaya untuk memerangi penyebaran
 konten pornografi yang semakin marak melalui jaringan internet dan perpesanan pribadi (private messaging).
, hal ini ditambah watak masyarakat Indonesia yang suka akan hal-hal sensual, seakan tidak pernah habisnya.
 Tidak-kah pernah kita berpikir bagaimana apabila anak-anak kecil menonton konten terlarang ini? Kita selalu
 mengeluh masalah pornografi, namun saat kasus ini terjadi, masalah ini justru dibesar-besarkan, kontenny disebar tanpa batas.
<br>    
Sebab, penyebaran yang massif dari konten pornografi akan membahayakan dan berdampak negatif kepada potensi kemajuan bangsa
 Indonesia. Bahkan, konten pornografi bisa merusak potensi positif generasi muda, baik secara fisik dan mental, untuk menyambut masa depan yang lebih baik.
<br>
Karena itu, Direktur Jenderal Aplikasi Informatika (Dirjen Aptika) Kementerian Komunikasi dan Informatika (Kemenkominfo)
 Semuel Abrijani Pangerapan pun kembali mengajak seluruh masyarakat di Tanah Air untuk bekerja sama dalam memberantas
 penyebaran konten pornografi. "Mari bahu-membahu turut memberantas penyebaran konten porno," ajak dia, di Kantor Kemenkominfo, Jakarta Pusat, Rabu (12/9).
Menurut Semmy, panggilan akrab Semuel, saat ini, Kemenkominfo tengah giat menggalakkan program literasi digital
 mengenai penggunaan internet secara tepat dan positif. Harapannya, ke depan, masyarakat Indonesia bisa bertransformasi total
 ke era digital dan memanfaatkan segala sesuatu menjadi yang produktif dengan ide-ide kreatif dan inovatif.

<br>
Pada acara diskusi Diskusi Pencegahan dan Penanganan Masalah Pornografi bertema Membangun Anak Bangsa yang Cerdas,
 Berkualitas, dan Berprestasi di Era Digital, itu hadir pula Pendiri The Lighted Candle Society John L Harmer.
 Mantan Wakil Gubernur California Amerika Serikat tersebut memberikan pandangannya mengenai dampak negatif dari menonton konten porno bagi remaja di Amerika Serikat.
Semmy mengungkapkan, saat ini, penyebaran konten pornografi melaui media internet telah berkembang menjadi aktivitas yang mengarah ke porno (pornoaksi).
 "Sekarang, bahkan, pelaku pornoaksi datang dari kalangan anak SD," tambah Dirjen Aptika.
Dia juga menyampaikan, tantangan yang dihadapi saat ini adalah penyebaran konten ponrografi melalui perpesanan pribadi (private messaging).
 Hal tersebut menjadi persoalan, baik bagi Kemenkominfo dan masyarakat, yang peduli karena akan lebih sulit pengawasannya.
"Salah satu persoalan jika konten-konten porno tersebut di-sharing pada ranah privat melalui messaging.
 Sebab, bagaimana pun penggunaan internet dan seluruh aplikasinya menjadi hak setiap warga negara, dan pemerintah tidak bisa begitu saja melakukan intervensi," ungkapnya.
Sasaran Remaja Sementara itu, Semmy juga mengingatkan remaja Indonesia agar tidak mengonsumsi konten pornografi di internet Sebab,
 kegiatan tersebut bisa merusak sel-sel otak selama masa pertumbuhan jika sering dilakukan.

<br>
Mengutip hasil riset Kementerian Pemberdayaan Perempuan dan Perlindungan Anak, Dirjen Aptika menjelaskan bahwa konsumsi konten pornografi 
sangat berbahaya bagi perkembangan otak, terutama pada remaja yang tengah dalam masa pertumbuhan fisik dan mental.
"Pada otak bagian depan sebagai pusat decision making, terjadi perusakan sel pada otak remaja yang adiksi terhadap konten porno. 
Pada lapisan terluar otak, atau yang disebut dengan "materi abu-abu akan semakin menipis pada remaja yang adiksi terhadap konten porno," jelasnya.
Untuk mengurangi peredaran konten pornografi, Kemenkominfo pun telah memberlakukan kebijakan penapisan dan menemukan lebih dari 1 juta
 website yang mempromosikan konten pornografi. Bahkan, sejak Agustus 2018, Kemenkomnifo telah menerapkan metode Forced 
 Save Search Engine untuk membuat pencarian hal-hal berbau porno di internet menjadi tidak berjalan.
"Tapi, kami tidak menutup konten-konten berbau kesehatan karena pendidikan seks juga penting. Yang kami fokuskan adalah menutup konten-konten berbau porno," tandas Semmy.

<br>
<b>Kerja Sama</b>
<br>
Sejak 6 Agustus 2018, Kemenkominfo bekerja sama dengan operator telekomunikasi dan Asosiasi Penyedia Jasa Internet Indonesia (APJII)
 telah mulai menapis konten pornografi dalam fitur pencarian aman laman di internet (safe search), terutama melalui Google. 
 Upaya tersebut dilakukan untuk menekan beredarnya pornografi melalui media internet.
Penapisan konten negatif dilakukan karena semakin banyak laporan dari pengguna Instagram bahwa konten pornografi tidak 
lagi harus diakses dengan kata kunci (keyword), melainkan juga bisa langsung ke gambarnya. "Sekarang kan, mereka menaruhnya 
bukan di kata-kata eksplisit tadi. Tapi sudah di lain-lain, memang kita sudah kucing-kucingan terus," ungkapnya.
Menurut dia, penghilangan gambar pornografi bukan terjadi di fitur pencarian website, melainkan ada pada penyedia jasa internet 
"Ini bukan di Google nya, tapi di ISP-nya. Kalau kita blokir kan di ISP-nya," kata Semmy.
Upaya penapisan yang dilakukan oleh Kemenkominfo, operator, dan APJII pun diharapkan akan langsung menyaring konten pornografi
 dari produsernya sebelum internet diakses oleh masyarakat. Konten tersebut diharapkan bisa dihilangkan, atau setidaknya sesedikit mungkin yang sampai ke masyarakat.

<br>
"Jadi, kalau masyarakat melakukan search dengan kata-kata pornografi, dan kemudian pindah ke image (menu) nya, 
diharapkan tidak ada lagi gambar-gambar berbau porno," tambahnya.
Secara teknis. Dirjen Aptika menjelaskan, ketika akan dilakukan pencarian oleh pengguna internet di Indonesia,
 gambar yang mengandung pornografi sudah tidak akan muncul lagi. "Jadi, eksplisitnya, gambarnya tidak ada lagi, seperti itu.
 Nanti, gambarnya bisa hilang, atau di-blur," jelasnya, seraya menunjukkan fitur safe search on yang bisa menghilangkan gambar 
 bermuatan pornografi dari hasil pencarian di peramban.

